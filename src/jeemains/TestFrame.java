    public void refreshButtonColors() {

        Color color;
        if (sub == Subject.P) {
            index = 0;
        } else if (sub == Subject.C) {
            index = 1;
        } else {
            index = 2;
        }

        for (int i = 0; i < 30; ++i) {
            if (currentSubject[index].question[i].state == Question.State.answered) {
                color = Color.GREEN;
                changeButtonColor(i, color);
            } else if (currentSubject[index].question[i].state == Question.State.guessed) {
                color = Color.YELLOW;
                changeButtonColor(i, color);
            } else if (currentSubject[index].question[i].state == Question.State.seen) {
                color = Color.RED;
                changeButtonColor(i, color);
            } else {
                color = Color.cyan;
                changeButtonColor(i, color);
            }
        }

        questionNo = 1;

    }

    public void loadQuestion() {

        optionSelected1.setSelected(false);
        optionSelected2.setSelected(false);
        optionSelected3.setSelected(false);
        optionSelected4.setSelected(false);
        testRadio.setSelected(true);

        getButtonFromNumber(questionNo).setBackground(Color.blue);

        updateDisplayVariables();

        try {

            Class.forName("java.sql.Driver");
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/gaurav", "root", "");
            Statement statement = connection.createStatement();

            String sql1 = "SELECT * FROM GUESS" + sub + " WHERE " + COL_QUES_NO + " = " + questionNo + " ;";
            //System.out.println(""+sql);

            ResultSet resultSet = statement.executeQuery(sql1);

            if (resultSet.next()) {
                int tempResult = resultSet.getInt(COL_ANS);

                switch (tempResult) {
                    case 1:
                        optionSelected1.setSelected(true);
                        break;
                    case 2:
                        optionSelected2.setSelected(true);
                        break;
                    case 3:
                        optionSelected3.setSelected(true);
                        break;
                    case 4:
                        optionSelected4.setSelected(true);
                        break;
                    default:
                        testRadio.setSelected(true);
                }
            } else {
                testRadio.setSelected(true);

            }

            connection.close();
            resultSet.close();
            statement.close();
        } catch (Exception e) {

            System.out.println("" + e);
        }

        questionNoLabel.setText("Q. " + questionNo);

        String quesPicName;
        quesPicName = "img" + sub + questionNo;
        //System.out.println(""+quesPicName);
        //quesPicName = "trytry";
        ImageIcon icon = new ImageIcon(getClass().getResource("/QuestionImages/" + quesPicName + ".jpg"));
        Image i = icon.getImage().getScaledInstance(questionImageLabel.getWidth(), questionImageLabel.getHeight(), Image.SCALE_AREA_AVERAGING);

        questionImageLabel.setIcon(new ImageIcon(i));

    }

