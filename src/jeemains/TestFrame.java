    public void colorChange() {
        if (currentSubject[index].question[questionNo - 1].state == Question.State.answered) {
            getButtonFromNumber(questionNo).setBackground(Color.GREEN);
        } else if (currentSubject[index].question[questionNo - 1].state == Question.State.guessed) {
            getButtonFromNumber(questionNo).setBackground(Color.YELLOW);
        } else {
            currentSubject[index].question[questionNo - 1].state = Question.State.seen;
            getButtonFromNumber(questionNo).setBackground(Color.RED);
        }
    }

